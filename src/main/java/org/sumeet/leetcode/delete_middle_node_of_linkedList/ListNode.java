package org.sumeet.leetcode.delete_middle_node_of_linkedList;

public class ListNode {

    int val;
    ListNode next;

    public ListNode() {
    }
    public ListNode(int val) {
        this.val = val;
    }
    public ListNode(int val, ListNode next) {
        this.val = val; this.next = next;
    }

}
